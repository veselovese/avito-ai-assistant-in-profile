import type { Ad, AutoAdParams, RealEstateAdParams, ElectronicsAdParams } from '../../../entities/ad/model/types';

// --- Вспомогательная функция для красивого форматирования параметров ---
const formatParams = (ad: Ad): string => {
  const p = ad.params;
  let details = '';

  if (ad.category === 'auto') {
    const params = p as AutoAdParams;
    details = `
    - Марка: ${params.brand || 'Не указано'}
    - Модель: ${params.model || 'Не указано'}
    - Год: ${params.yearOfManufacture || 'Не указано'}
    - Коробка: ${params.transmission || 'Не указано'}
    - Пробег: ${params.mileage ? `${params.mileage} км` : 'Не указано'}
    - Мощность: ${params.enginePower ? `${params.enginePower} л.с.` : 'Не указано'}`;
  } else if (ad.category === 'real_estate') {
    const params = p as RealEstateAdParams;
    details = `
    - Тип: ${params.type || 'Не указано'}
    - Адрес: ${params.address || 'Не указано'}
    - Площадь: ${params.area ? `${params.area} м²` : 'Не указано'}
    - Этаж: ${params.floor || 'Не указано'}`;
  } else if (ad.category === 'electronics') {
    const params = p as ElectronicsAdParams;
    details = `
    - Тип: ${params.type || 'Не указано'}
    - Бренд: ${params.brand || 'Не указано'}
    - Модель: ${params.model || 'Не указано'}
    - Состояние: ${params.condition || 'Не указано'}
    - Цвет: ${params.color || 'Не указано'}`;
  }

  return details;
};

// --- 1. Промпт для генерации описания ---
export const getDescriptionPrompt = (ad: Ad): string => {
  const paramsText = formatParams(ad);

  return `
Ты — профессиональный копирайтер для маркетплейса (Авито, Юла). Твоя задача — написать или улучшить описание товара, чтобы его быстрее купили.

ДАННЫЕ ТОВАРА:
Название: ${ad.title}
Категория: ${ad.category === 'auto' ? 'Автомобили' : ad.category === 'real_estate' ? 'Недвижимость' : 'Электроника'}
Текущая цена: ${ad.price} ₽
Характеристики:${paramsText}
Текущее описание продавца: "${ad.description || '(Пусто)'}"

ТРЕБОВАНИЯ К ТЕКСТУ:
1. Напиши на русском языке.
2. Используй дружелюбный, но деловой тон.
3. Структурируй текст: начни с привлекательного вступления, затем перечиши характеристики (преобразуя их в продающие преимущества), и в конце призови к действию ("Пишите", "Звоните").
4. НЕ ИСПОЛЬЗУЙ HTML-теги (например, <b>, <br>, <p>, <li>). Используй обычный текст с переносами строк.
5. Не выдумывай характеристики, которых нет в "ДАННЫХ ТОВАРА", но подчеркивай достоинства.
6. Если текущее описание хорошее, сделай его более читаемым и структурированным, сохраняя смысл.

ОТВЕТ:
Верни только готовый текст описания без вступительных фраз и пояснений.
`.trim();
};

